# luodaan lista, jossa on 4 alkiota

lista = ["Sininen", "Punainen", "Keltainen", "Vihreä"]

# tulostetaan listan ensimmäinen alkio
print("Listan ensimmäinen alkio on:", lista[0])

# tulostetaan listan alkiot yksi kerrallaan
print("Lista tulostettuna alkio kerrallaan:")
for alkio in lista:
    print(alkio)
